// Fonctionnalités Attendues :

// Ajouter un livre à la liste :
// L'utilisateur saisit le titre, l'auteur, et l'année de publication d'un livre dans un formulaire.
// En cliquant sur le bouton "Envoyez", le livre est ajouté à la liste affichée sur la page et à un tableau de livres stocké en mémoire.
// Si les champs ne sont pas remplis ou si le livre existe déjà, une alerte s'affiche.
// Afficher tous les livres ajoutés :

// Les livres ajoutés s'affichent dans une table avec des colonnes pour le titre, l'auteur et l'année de publication.
// Chaque ligne du tableau représente un livre, et contient également un bouton "X" pour supprimer le livre.

// Supprimer un livre de la liste :
// En cliquant sur le bouton "X" à côté d'un livre, celui-ci est supprimé de la liste affichée et du tableau stocké en mémoire.

// Gestion des alertes utilisateur :
// Après chaque action (ajout ou suppression), une alerte apparaît pour informer l'utilisateur du succès ou de l'erreur de l'opération (par exemple, "Livre ajouté", "Remplissez les champs", ou "Ce livre existe déjà").
